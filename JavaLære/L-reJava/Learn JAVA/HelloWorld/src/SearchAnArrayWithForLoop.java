
public class SearchAnArrayWithForLoop { // Searchin an array with loops!
	public static void main(String[] args) {
		
		int[] grades = {1,2,3};
		
		for(int i = 0; i < grades.length; i++) {
			if(grades[i] == 3) {
				System.out.println("FOUND! Found at Index " + i);
				
			}
			
			
		}
		
		
	}

}
